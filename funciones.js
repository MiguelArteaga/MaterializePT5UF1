function reservas1(id) {
	if (id == 'm1c1'){
		document.getElementById(id).className='mesaReservada1';
	}
	else if(id == 'm1c2'){
		document.getElementById(id).className='mesaReservada2';
	}
	else if(id == 'm1c3'){
		document.getElementById(id).className='mesaReservada3';
	}
	else if(id == 'm1c4'){
		document.getElementById(id).className='mesaReservada4';
	}
}

